using ll = long long;

const int MxN = 100010;
ll a[MxN];

inline void solution(){
	int n;
	ll k, now, maxx = 0ll;
	cin >> n >> k;
	for(int i=1; i<=n; ++i){
		cin >> a[i];
	}
	for(int l=1; l<=n; ++l){
		now = 0ll;
		for(int r=l; r<=n; ++r){
			now = (now + a[r]) % k;
			maxx = max(maxx, now);
		}
	}
	cout << maxx;
	return ;
}

signed main(){
	cin.tie(nullptr)->ios::sync_with_stdio(false);	
	int q = 1;
//	cin >> q;
	while(q--){
		solution();
		cout << "\n";
	}
	return 0;
}
