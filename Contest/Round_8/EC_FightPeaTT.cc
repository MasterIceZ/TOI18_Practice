using ll = long long;

inline void solution(){
	int x, cnt = 0;
	cin >> x;
	if(x == 1){
		cout << 2;
		return ;
	}
	int sq = sqrt(x);
	for(int i=2; i<=sq; ++i){
		if(x % i == 0){
			cnt++;
			while(x % i == 0){
				x /= i;
			}
		}
	}
	if(x != 1){
		cnt++;
	}
	cout << (cnt % 3 == 1? 2: 1);
	return ;
}

signed main(){
	cin.tie(nullptr)->ios::sync_with_stdio(false);	
	int q = 1;
	cin >> q;
	while(q--){
		solution();
		cout << "\n";
	}
	return 0;
}
